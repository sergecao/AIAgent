class TextProcessor:
    @staticmethod
    def get_prompt(text: str, mode: str) -> str:
        """Формирование промпта в зависимости от режима"""
        prompts = {
            "улучшить": f"Улучши следующий текст, сделай его более профессиональным и грамматически правильным:\n\n{text}",
            "кратко": f"Изложи кратко основную информацию из следующего текста:\n\n{text}",
            "перевести": f"Переведи следующий текст на английский язык:\n\n{text}"
        }
        return prompts.get(mode, f"Обработай следующий текст согласно запросу '{mode}':\n\n{text}")